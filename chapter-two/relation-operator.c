/*
realation operator
== : for equating two values (for every integer it's true but for 0 it's false)
<, <=  : greator than & greator than equal too
>, >=  : less than & less than equal too
!= : not equal too
*/
#include<stdio.h>
int main(){
    printf("%d\n" ,4 == 4);
    printf("%d\n" ,3 < 2);
    printf("%d\n" ,5 <= 6);
    printf("%d\n" ,7 > 4);
    printf("%d\n" ,9 >= 4);
    printf("%d\n" ,4 != 4);
    return 0;
}